use std::sync::{Mutex, OnceLock};

#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum Lang {
    En,
    ZhCn,
}

static LANG: OnceLock<Mutex<Lang>> = OnceLock::new();

fn store() -> &'static Mutex<Lang> {
    LANG.get_or_init(|| Mutex::new(Lang::En))
}

pub fn set_language(lang: Option<&str>) {
    let env_lang = std::env::var("RUSTMODLICA_LANG").ok();
    let picked = lang.or(env_lang.as_deref()).unwrap_or("en");
    let parsed = parse_lang(picked);
    if let Ok(mut g) = store().lock() {
        *g = parsed;
    }
}

fn parse_lang(v: &str) -> Lang {
    let s = v.to_ascii_lowercase();
    if s.starts_with("zh") {
        Lang::ZhCn
    } else {
        Lang::En
    }
}

fn current() -> Lang {
    store().lock().map(|g| *g).unwrap_or(Lang::En)
}

#[allow(dead_code)]
pub fn current_lang_code() -> &'static str {
    match current() {
        Lang::En => "en",
        Lang::ZhCn => "zh-CN",
    }
}

