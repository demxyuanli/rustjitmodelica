fn try_read_regression_data_root(config_path: &Path) -> Option<PathBuf> {
    let text = fs::read_to_string(config_path).ok()?;
    let v: Value = serde_json::from_str(&text).ok()?;
    let p = v
        .get("defaults")
        .and_then(|d| d.get("regression_data_root"))
        .and_then(|x| x.as_str())
        .or_else(|| {
            v.get("defaults")
                .and_then(|d| d.get("regression_data_root"))
                .and_then(|x| x.as_str())
        })?;
    if p.trim().is_empty() {
        None
    } else {
        Some(PathBuf::from(p))
    }
}

pub fn resolve_scope(repo_root: &Path, name: &str) -> Result<ScopeResolved> {
    let n = name.trim().to_ascii_lowercase();
    let Some(spec) = list_scopes()
        .into_iter()
        .find(|s| s.name.eq_ignore_ascii_case(n.as_str()))
    else {
        bail!("unknown scope: {name}");
    };

    let config_path = match spec.source {
        ScopeSource::StaticConfig { path } => repo_root.join(path),
        ScopeSource::GeneratedPs1 { key } => crate::ps1_profiles::ensure_profile(repo_root, key)?
            .with_context(|| format!("ps1 profile not available: {key}"))?,
    };

    let data_root = try_read_regression_data_root(&config_path);

    Ok(ScopeResolved {
        name: spec.name.to_string(),
        desc: spec.desc.to_string(),
        config_path,
        data_root,
        workers: None,
        format: OutputFormat::Human,
        progress: true,
    })
}

